// Utility to get the best language match
function getBestLanguage() {
  const browserLangs = navigator.languages ? navigator.languages : [navigator.language || navigator.userLanguage];
  for (const lang of browserLangs) {
    let code = lang.split('-')[0];
    if (code === 'no') code = 'nb';
    if (SUPPORTED_LANGUAGES.includes(code)) return code;
  }
  return 'en'; // fallback
}

// Export for use in main script
window.LotteryTranslations = { translations, SUPPORTED_LANGUAGES, getBestLanguage };
